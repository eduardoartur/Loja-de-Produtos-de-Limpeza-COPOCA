<p>
  Para a limpeza de piso cerâmico indico o uso de três tipos de detergente. De acordo com cada sujidade existem tipos de detergente específicos e apropriados.<br><br> <b>Existem três tipos de detergente:</b><br><br> <b>Detergente Ácido:</b> é indicado para sujidades de composição mineral: cimento, óleos, graxas e demais derivados de petróleo. A diluição deve ser feita corretamente porque o mal uso pode danificar o piso. É indicado para limpeza pós obra ou limpeza pesada e tem seu PH entre 0 e 7.<br><br> <b>Detergente Neutro:</b> Não agride o piso, pois não realiza limpeza agressiva. É indicado para limpeza diária, mas tem poder de remoção elevado com ph estabilizado em 7.<br><br> <b>Detergente Alcalino:</b> Remove todo tipo de sujidade, com exceção das de origem mineral. Deve ser usado com cuidado para não agredir o piso. Tem seu ph entre 7 e 14 e é indicado para superfície com alto teor de gordura e sujidade incrustada.<br><br> O uso correto do detergente pode garantir uma superfície limpa, bonita e com aparência de nova por longos anos.<br><br> Esteja atento ao ph e a diluição descrita no rótulo do produto. Facilita 
