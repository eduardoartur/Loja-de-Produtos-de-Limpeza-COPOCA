<div id="section_conteudo">
    <p> Álcool nada mais é do que um desinfetante. Muitas vezes não serve apenas para limpar locais com bactérias, mas também para realizar a limpeza de vidros, metais e espelhos. Pode ser utilizado direto nas superfícies com um pano úmido.<br>
<b>Onde usar:</b> na limpeza de locais infectados e também de vidros e metais.<br>
<b>Truque:</b> não utilizar na limpeza de áreas muito grandes, pois seu tempo de evaporação é muito curto.<br>
<b>Cuidados:</b> não utilizar em superfícies emborrachadas, pois danifica esse tipo de material.<br><br>

<b>ÁLCOOL GEL</b><br>
Com apenas 70% de pureza na fórmula, a queima é menos intensa e a evaporação é mais lenta, tornando esse tipo de álcool mais eficiente para desinfetar. Ele é o ideal para a limpeza, principalmente doméstica. Também é mais em conta, se comparado aos demais tipos.<br><br>

<b>ÁLCOOL HIDRATADO</b><br>
Altamente inflamável, com 95% de pureza. Os outros 5% da solução incluem água e um aromatizante malcheiroso para evitar que as pessoas bebam o produto.<br><br>

<b>ÁLCOOL ANIDRO</b>
Com 100% de pureza, esse álcool de uso industrial é fogo na certa. Só não serve para limpeza, já que evapora instantaneamente.<br><br>

<b>Quando evitar</b>
O álcool não deve ser usado nos seguintes materiais, sob risco de danificação:<br><br>

 <ul>
  <li>Fórmica</li>
 <li>Borracha</li>
<li>Acrílico</li>
<li>Superfície de aparelhos eletrônicos</li>
</ul><br><br>
<b>Como utilizar</b><br>
No caso de tecidos, estofados, espelhos, vidros, pia e eletrodomésticos, dentre outros, o álcool não oferece tantos danos e pode ser utilizado como agente desinfetante, porém com cautela. As orientações para esses casos são:<br><br>
<ul>
 <li>Opte pelo álcool gel, que é mais seguro, já que leva mais tempo para alastrar o fogo.</li>
 <li>Leia a embalagem antes da utilização e verifique os locais em que o produto pode ser aplicado.</li>
 <li>Faça pequenos testes no objeto em questão, já que o álcool pode manchar facilmente certos materiais.</li>
 <li>O álcool evapora rapidamente e não deixa resíduos na superfície. O ideal é aplicá-lo com um pano umedecido ou diretamente no local.</li>
 <li>Para usar em tecidos, como roupas, sofás e teto do carro, dilua o produto em água e aplique com um borrifador.</li>
<li>Armazene o frasco em uma área bem ventilada para evitar incêndios.</li>
</ul>
<b>Cuidados Importantes:</b><br><br>

Desde 2002 diversas entidades da sociedade civil organizada reunidas na Frente Nacional de Combate aos Acidentes com Álcool tentam convencer os brasileiros e os parlamentares do país de que o uso dessa substância deveria ser evitado.<br><br>

O álcool é um produto altamente inflamável e um dos principais responsáveis por acidentes com queimaduras graves em adultos e crianças, no entanto, o álcool  com graduação entre 46 a 65 graus exterminaram entre 98 e 99,6% dos microrganismos, segundo a Proteste – Associação Brasileira de Defesa do Consumidor. Esse dado é comparável ao resultado de outros produtos de limpeza bactericidas.<br><br>

Para Eraldo de Melo, diretor da empresa Conservar, em grande parte das vezes, o uso do álcool na limpeza não se justifica. “O álcool não age diretamente como agente de limpeza, apesar de muitas pessoas acharem que sim. O uso inadequado pode trazer riscos à saúde e danificar móveis. Os produtos específicos para a limpeza da casa, como detergente ou limpador geral, possuem ação bactericida, preservando móveis, equipamentos e utensílios, e não são inflamáveis”, explica.<br><br>

Mas não são todos que dividem essa opinião. Se você é daquelas pessoas que não abre mão no álcool na limpeza doméstica, fique atendo na diferença na utilização do álcool em gel e do álcool líquido. O álcool líquido tem 95% de pureza e, por isso, pega fogo rápido e evapora muito depressa, tornando o produto menos eficiente para limpeza do que o gel”, explica Henrique Eisi Toma, do Instituto de Química da USP. Já o álcool em gel, atualmente, existem duas versões no mercado. O de farmácia para desinfetar as mãos e some frequentemente das prateleiras, nas ondas de gripe, e contém ingredientes que evitam o ressecamento da pele, como a trictanolamina, e aromatizantes. Além do álcool em gel para faxina.<br><br>

O que especialista defendem é que quanto menos inflamável for o álcool, mais eficiente será a limpeza. </p>
